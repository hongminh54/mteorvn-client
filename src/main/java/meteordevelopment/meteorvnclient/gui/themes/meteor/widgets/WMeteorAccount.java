package meteordevelopment.meteorvnclient.gui.themes.meteor.widgets;

import meteordevelopment.meteorvnclient.gui.WidgetScreen;
import meteordevelopment.meteorvnclient.gui.themes.meteor.MeteorWidget;
import meteordevelopment.meteorvnclient.gui.widgets.WAccount;
import meteordevelopment.meteorvnclient.systems.accounts.Account;
import meteordevelopment.meteorvnclient.utils.render.color.Color;

public class WMeteorAccount extends WAccount implements MeteorWidget {
    public WMeteorAccount(WidgetScreen screen, Account<?> account) {
        super(screen, account);
    }

    @Override
    protected Color loggedInColor() {
        return theme().loggedInColor.get();
    }

    @Override
    protected Color accountTypeColor() {
        return theme().textSecondaryColor.get();
    }
}
